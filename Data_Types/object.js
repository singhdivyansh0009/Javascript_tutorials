/* 
 Objects in javascript are variables too. But objects can contain many values.
 Object values are written as key : value pairs (name and value separated by a colon).
*/
const items = {
    fruit : "mango",
    vegetables : "Potato",
    marks : 70,
    value : true
}
console.log(items);  // printing whole object
console.log(items["fruit"]); // printing particular key
console.log(items["vegetables"]);